namespace DigitalRune.Windows.Controls
{
    /// <summary>
    /// Defines the view modes of a <see cref="ExplorerView"/>.
    /// </summary>
    public enum ExplorerViewMode
    {
        /// <summary>Show items as tiles.</summary>
        Tiles,

        /// <summary>Show items in a detailed list with multiple columns.</summary>
        Details,

        /// <summary>Show items in a simple list.</summary>
        List,

        /// <summary>Show items as small icons.</summary>
        SmallIcons,

        /// <summary>Show items as medium icons.</summary>
        MediumIcons,

        /// <summary>Show items as large icons.</summary>
        LargeIcons,

        /// <summary>Show items as extra large icons.</summary>
        ExtraLargeIcons,
    }
}
