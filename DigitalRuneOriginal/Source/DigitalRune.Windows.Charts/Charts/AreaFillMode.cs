namespace DigitalRune.Windows.Charts
{
    /// <summary>
    /// Defines how the area in a <see cref="ColoredLineChart"/> is filled.
    /// </summary>
    public enum AreaFillMode
    {
        /// <summary>
        /// The area is filled with a solid color.
        /// </summary>
        Solid,

        /// <summary>
        /// The area is filled with a horizontal gradient.
        /// </summary>
        GradientHorizontal,

        /// <summary>
        /// The area is filled with a vertical gradient.
        /// </summary>
        GradientVertical,
    }
}
